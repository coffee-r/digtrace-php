CREATE TABLE shop_customers (
  id NUMBER PRIMARY KEY,
  name VARCHAR2(100) NOT NULL,
  email VARCHAR2(200),
  points NUMBER DEFAULT 0 NOT NULL,
  credit_remaining NUMBER DEFAULT 0 NOT NULL,
  is_first_time CHAR(1) DEFAULT 'N' NOT NULL
);

CREATE TABLE shop_addresses (
  id NUMBER PRIMARY KEY,
  customer_id NUMBER,
  postal_code VARCHAR2(7) NOT NULL,
  prefecture VARCHAR2(40) NOT NULL,
  is_self CHAR(1) DEFAULT 'Y' NOT NULL
);

CREATE TABLE shop_products (
  code VARCHAR2(40) PRIMARY KEY,
  name VARCHAR2(120) NOT NULL,
  price NUMBER DEFAULT 0 NOT NULL,
  one_per_customer_qty CHAR(1) DEFAULT 'N' NOT NULL,
  one_per_customer_once CHAR(1) DEFAULT 'N' NOT NULL,
  yumail_single_eligible CHAR(1) DEFAULT 'N' NOT NULL,
  gift_trigger CHAR(1) DEFAULT 'N' NOT NULL,
  is_gift CHAR(1) DEFAULT 'N' NOT NULL,
  is_point_exchange CHAR(1) DEFAULT 'N' NOT NULL,
  point_cost NUMBER DEFAULT 0 NOT NULL,
  is_variety CHAR(1) DEFAULT 'N' NOT NULL,
  variety_group VARCHAR2(40),
  is_reserved CHAR(1) DEFAULT 'N' NOT NULL,
  is_set CHAR(1) DEFAULT 'N' NOT NULL,
  sale_start_date VARCHAR2(10),
  sale_end_date VARCHAR2(10)
);

CREATE TABLE shop_product_components (
  parent_code VARCHAR2(40) NOT NULL,
  component_code VARCHAR2(40) NOT NULL,
  component_name VARCHAR2(120) NOT NULL,
  sort_order NUMBER NOT NULL
);

CREATE TABLE shop_cart_items (
  id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  cart_id VARCHAR2(80) NOT NULL,
  customer_id NUMBER,
  product_code VARCHAR2(40) NOT NULL,
  quantity NUMBER NOT NULL,
  unit_price NUMBER NOT NULL,
  is_gift CHAR(1) DEFAULT 'N' NOT NULL,
  created_at TIMESTAMP NOT NULL
);

CREATE TABLE shop_reservation_cart_items (
  id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  cart_id VARCHAR2(80) NOT NULL,
  customer_id NUMBER,
  product_code VARCHAR2(40) NOT NULL,
  quantity NUMBER NOT NULL,
  unit_price NUMBER NOT NULL,
  is_gift CHAR(1) DEFAULT 'N' NOT NULL,
  created_at TIMESTAMP NOT NULL
);

CREATE TABLE shop_orders (
  id NUMBER PRIMARY KEY,
  customer_id NUMBER,
  address_id NUMBER,
  email VARCHAR2(200),
  shipping_method VARCHAR2(20) NOT NULL,
  payment_method VARCHAR2(30) NOT NULL,
  subtotal NUMBER NOT NULL,
  shipping_fee NUMBER NOT NULL,
  status VARCHAR2(30) NOT NULL,
  payment_status VARCHAR2(30) NOT NULL,
  delivery_date VARCHAR2(10),
  delivery_time VARCHAR2(20),
  created_at TIMESTAMP NOT NULL
);

CREATE TABLE shop_order_items (
  id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  order_id NUMBER NOT NULL,
  product_code VARCHAR2(40) NOT NULL,
  name VARCHAR2(120) NOT NULL,
  quantity NUMBER NOT NULL,
  unit_price NUMBER NOT NULL,
  is_gift CHAR(1) DEFAULT 'N' NOT NULL
);

CREATE TABLE shop_working_days (
  work_date DATE PRIMARY KEY,
  is_working CHAR(1) DEFAULT 'Y' NOT NULL
);

CREATE TABLE shop_delay_prefectures (
  prefecture VARCHAR2(40) PRIMARY KEY,
  active CHAR(1) DEFAULT 'Y' NOT NULL
);

CREATE TABLE shop_remote_island_postals (
  postal_code VARCHAR2(7) PRIMARY KEY
);

